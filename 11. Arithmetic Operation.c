#include <stdio.h>
int main() 
{
int n1,n2; //performing various arithmetic operation
printf("Enter first number = \n");
scanf("%d",&n1);
printf("Enter second number = \n");
scanf("%d",&n2);
printf("The sum of %d and %d is %d\n",n1,n2,n1+n2);
printf("The difference of %d and %d is %d\n",n1,n2,n1-n2);
printf("The product of %d and %d is %d\n",n1,n2,n1*n2);
printf("The quotient of %d and %d is %d\n",n1,n2,n1/n2);
printf("The remainder of %d and %d is %d \n",n1,n2,n1%n2);
}
